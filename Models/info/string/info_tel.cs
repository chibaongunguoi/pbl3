namespace module_info;

sealed class InfoTel : InfoString
{
    // ========================================================================
    public InfoTel() { }

    // ------------------------------------------------------------------------
    public InfoTel(string content)
        : base(content) { }

    // ========================================================================
    public override string get_pattern()
    {
        return @"^\d{10-11}$";
    }

    // ========================================================================
}

