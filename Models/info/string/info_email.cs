namespace module_info;

sealed class InfoEmail : InfoString
{
    // ========================================================================
    public InfoEmail() { }

    // ------------------------------------------------------------------------
    public InfoEmail(string content)
        : base(content) { }

    // ========================================================================
    public override string get_pattern()
    {
        return @"^[^@\s]+@[^@\s]+\.[^@\s]+$";
    }

    // ========================================================================
}

